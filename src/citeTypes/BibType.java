package citeTypes;
/*
 * Different bibtex-types.
 * We do not feature all types, only the ones listed below:
 * inproceedings: Conference paper in a proceeding
 * book: Complete book
 * article: Journal paper
 * inbook: Chapter in a book
 * phdthesis: A PhD thesis
 * techreport: A technical report
 */
public enum BibType {
	
	inproceedings, book, article, inbook, phdthesis, techreport
	
}
